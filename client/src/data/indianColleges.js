// Local storage key for dynamic colleges
const DYNAMIC_COLLEGES_KEY = 'kaizen_dynamic_colleges';

// Get dynamic colleges from localStorage
export const getDynamicColleges = () => {
  try {
    const stored = localStorage.getItem(DYNAMIC_COLLEGES_KEY);
    return stored ? JSON.parse(stored) : [];
  } catch (error) {
    console.error('Error loading dynamic colleges:', error);
    return [];
  }
};

// Save dynamic colleges to localStorage
export const saveDynamicColleges = (colleges) => {
  try {
    localStorage.setItem(DYNAMIC_COLLEGES_KEY, JSON.stringify(colleges));
  } catch (error) {
    console.error('Error saving dynamic colleges:', error);
  }
};

// Add a new college to dynamic list
export const addDynamicCollege = (collegeName) => {
  const dynamicColleges = getDynamicColleges();
  const trimmedName = collegeName.trim();

  // Check if already exists (case-insensitive)
  const exists = dynamicColleges.some(college =>
    college.toLowerCase() === trimmedName.toLowerCase()
  ) || indianColleges.some(college =>
    college.toLowerCase() === trimmedName.toLowerCase()
  );

  if (!exists && trimmedName.length >= 3) {
    dynamicColleges.push(trimmedName);
    saveDynamicColleges(dynamicColleges);
    return true;
  }

  return false;
};

// Get combined college list (static + dynamic)
export const getAllColleges = () => {
  const dynamicColleges = getDynamicColleges();
  return [...indianColleges, ...dynamicColleges];
};

// Function to search colleges with fuzzy matching (enhanced with dynamic colleges)
export const searchColleges = (query, limit = 10) => {
  if (!query || query.length < 2) return [];

  const searchTerm = query.toLowerCase().trim();
  const allColleges = getAllColleges();

  // Exact matches first
  const exactMatches = allColleges.filter(college =>
    college.toLowerCase().includes(searchTerm)
  );

  // Sort by relevance (shorter matches first, then alphabetical)
  const sortedMatches = exactMatches.sort((a, b) => {
    const aIndex = a.toLowerCase().indexOf(searchTerm);
    const bIndex = b.toLowerCase().indexOf(searchTerm);

    // Prioritize matches at the beginning
    if (aIndex !== bIndex) {
      return aIndex - bIndex;
    }

    // Then by length (shorter first)
    if (a.length !== b.length) {
      return a.length - b.length;
    }

    // Finally alphabetical
    return a.localeCompare(b);
  });

  return sortedMatches.slice(0, limit);
};

